import turtle

def buttons(x, y, direction):
    t.penup()
    t.goto(x, y)
    t.setheading(direction)
    t.forward(23)
    t.pendown()
    t.setheading(0)
    t.begin_fill()
    t.circle(12)
    t.end_fill()

